using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calc
{
    public class Calc
    {
        public int Sum(int a, int b)
        {
            return a + b;
        }

        public int Min(int a, int b)
        {
            return a - b;
        }

        public int Multi(int a, int b)
        {
            return a * b;
        }

        public double Devide(int a, int b)
        {
            return a / b;
        }
    }
}
